const calculadora={
    suma:function (numero1, numero2) {
        return numero1+numero2
    },
    resta:function (numero1, numero2) {
        return numero1-numero2
    },
    multiplicacion:function (numero1, numero2) {
        return numero1*numero2
    },
    division:function (numero1, numero2) {
        return numero1/numero2
    },

}
export{calculadora}